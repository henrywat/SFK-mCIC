package com.henrywat.cic.svr.model;

public class TeamList {

	private int idx;
	private String team;
	
	public TeamList() {}

	public int getIdx() {
		return idx;
	}

	public void setIdx(int idx) {
		this.idx = idx;
	}

	public String getTeam() {
		return team;
	}

	public void setTeam(String team) {
		this.team = team;
	}
}